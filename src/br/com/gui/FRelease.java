    public void BarraDeProgresso() {

        try {
            JProgressBar progresso = new JProgressBar();
            setSize(300, 70);
            add(progresso);
            setLocationRelativeTo(null);
            setCursor(new Cursor(Cursor.WAIT_CURSOR));
            setVisible(true);
            progresso.setToolTipText("Aguarde...");
            for (int i = 0; i < 100; i++) {
                progresso.setValue(i);
                Thread.sleep(100);
            }

        } catch (Exception e) {
            e.printStackTrace();
        }

    }


